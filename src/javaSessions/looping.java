package javaSessions;

public class looping 
{
	
	public static void main(String[] args)
	{
		
		
		int i=1;
		int j=i++;
		System.out.println(i);
		System.out.println(j);
		// OUTPUT : 2,1
		
		System.out.println("-----------------");
		
		int k=1;
		int l=++k;
		System.out.println(k);
		System.out.println(l);
		// OUTPUT : 1,2
		
	}

}
